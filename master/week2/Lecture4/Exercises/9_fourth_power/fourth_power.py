def square(x):
    """
    x: int or float
    return: x^2
    """
    return x * x
"""
End Runner Code
"""

def fourthPower(x):
    """
    Input:
    x: int or float.
    Returns: x ^ 4
    """
    return square(x) * square(x)

"""
Used for running, do not include when submitting to the grader.
"""
x = 2.0
print(fourthPower(x))
